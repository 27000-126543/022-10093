function buildCustomer(raw: typeof rawCustomerData[0]): Customer {
  const dealData = calculateDealProbability({
    budget_min: raw.budget_min,
    budget_max: raw.budget_max,
    decision_cycle: raw.decision_cycle,
    projects: raw.projects,
    skin_tags: raw.skin_tags,
    contour_tags: raw.contour_tags,
    channel: raw.channel,
  });

  const lastFollowDays = raw.lastFollowDaysAgo ?? (Date.now() - new Date(raw.createdDaysAgo).getTime()) / (1000 * 60 * 60 * 24);
  const tier = assignTier(dealData.total, lastFollowDays);

  const customer: Customer = {
    id: `customer-${raw.name}`,
    name: raw.name,
    phone: raw.phone,
    age: raw.age,
    gender: raw.gender,
    channel: raw.channel,
    budget_min: raw.budget_min,
    budget_max: raw.budget_max,
    decision_cycle: raw.decision_cycle,
    projects: raw.projects,
    skin_tags: raw.skin_tags,
    contour_tags: raw.contour_tags,
    tier,
    deal_probability: dealData.total,
    deal_probability_breakdown: dealData.breakdown,
    consultant_id: raw.consultant_id,
    created_at: daysAgo(raw.createdDaysAgo),
    last_follow_up: raw.lastFollowDaysAgo !== null ? daysAgo(raw.lastFollowDaysAgo) : null,
    status: raw.status,
  };

  return customer;
}

export const mockCustomers: Customer[] = rawCustomerData.map(buildCustomer);

